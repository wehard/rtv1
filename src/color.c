#include "vector.h"

int	ft_get_color(t_vec3 c)
{
	int ic;
	int r;
	int g;
	int b;

	if (c.x > 1.0)
		c.x = 1.0;
	if (c.y > 1.0)
		c.y = 1.0;
	if (c.z > 1.0)
		c.z = 1.0;

	r = ((int)(c.x * 255.0f) << 16);
	g = ((int)(c.y * 255.0f) << 8);
	b = ((int)(c.z * 255.0f));
	ic = r | g | b;
	if (ic < 0)
		return (0);
	return (ic);
}
