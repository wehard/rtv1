#include "vector.h"

int	ft_get_color(t_vec3 c)
{
	int r;
	int g;
	int b;

	r = (int)(c.x * 255.0f) << 16;
	g = (int)(c.y * 255.0f) << 8;
	b = (int)(c.z * 255.0f);
	return (r | g | b);
}
