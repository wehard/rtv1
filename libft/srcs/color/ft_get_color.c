#include "color.h"

int	ft_get_color(t_rgba c)
{
	int r;
	int g;
	int b;

	r = 0;
	g = 0;
	b = 0;
	r = (int)(c.r * 255.0f) << 16;
	g = (int)(c.g * 255.0f) << 8;
	b = (int)(c.b * 255.0f);
	return (r | g | b);
}
