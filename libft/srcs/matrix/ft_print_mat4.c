#include "matrix.h"
#include "ft_printf.h"

void	ft_print_mat4(t_mat4x4 m)
{
	int i;

	i = 0;
	ft_printf("---------------------------\n");
	while (i < 4)
	{
		ft_printf("%6.3f %6.3f %6.3f %6.3f\n",
			m.m[i][0], m.m[i][1], m.m[i][2], m.m[i][3]);
		i++;
	}
	ft_printf("---------------------------\n");
}
