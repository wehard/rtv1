double	ft_rand_d(int *seed)
{
	int a;

	seed[0] = 0x00269ec3 + seed[0] * 0x000343fd;
	a = (seed[0] >> 16) & 32767;
	return (-1.0f + (2.0 / 32767.0f) * (double)a);
}
